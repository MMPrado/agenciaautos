    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

// TODO add your handling code here:
        String enunciadoSQL;
        int pos; // posicion del registro seleccionado para eliminar
        int id;
        int nr;// numero de registros
        int idMarca;
        String marca;
        limpiarTabla();
        Object [][] datos;
        marca=txtMarca.getText();
        enunciadoSQL="SELECT * FROM marcas WHERE marca LIKE'%"+marca+"%' ";
        con.consultar(enunciadoSQL);
        nr=con.getNumRegistros();
        datos=new Object [nr][2];
        //tblDatos.setModel(new DefaultTableModel(datos));
        
        if (con.irPrimero()){
            for (int i = 0; i < nr; i++) {
                idMarca=Integer.parseInt(con.obtenerCampo("idmarca"));
                marca=con.obtenerCampo("marca");
                
                tblDatos.setValueAt(idMarca, i, 0);
                tblDatos.setValueAt(marca, i, 1);
                con.irSiguiente();
            }
            
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String enunciadoSQL;
         int pos; // posicion del registro seleccionado para eliminar
        int id;
        int op;//opcion de confirmacion
        
        pos=tblDatos.getSelectedRow();
        if (pos!=-1){
            
            op=JOptionPane.showConfirmDialog(this, "Estas seguro?");
            if (op==JOptionPane.OK_OPTION){
                id=Integer.parseInt(tblDatos.getValueAt(pos, 0).toString());
                enunciadoSQL="DELETE  FROM marcas WHERE idmarca="+id;
                con.actualizar(enunciadoSQL);
                limpiarTabla();
                this.btnBuscarActionPerformed(null);
                
            }
        }else{
            JOptionPane.showMessageDialog(this, "Seleccina un registro para eliminar");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

